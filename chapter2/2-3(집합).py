#집합
s1 = set([1, 2, 3, 4, 5, 6,])
s2 = set([4, 5, 6, 7, 8, 9,])
print(s1 & s2)
#>>>{4, 5, 6}

#합집합
s1 = set([1, 2, 3, 4, 5, 6,])
s2 = set([4, 5, 6, 7, 8, 9,])
print(s1 | s2)
#>>>{1, 2, 3, 4, 5, 6, 7, 8, 9}

#차집합
s1 = set([1, 2, 3, 4, 5, 6,])
s2 = set([4, 5, 6, 7, 8, 9,])
print(s1 - s2)
#>>>{1, 2, 3}

l1 = [1,2,2,3,3,3,4]
s1 = set(l1)
print(s1)
#>>>{1, 2, 3, 4}

#리스트로 변경하기
l1 = [1,2,2,3,3,3,4]
l1 = list(set(l1))
print(l1)
#>>>[1, 2, 3, 4]